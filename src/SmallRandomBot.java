
import afk.bot.london.TankRobot;

/**
 * Sample class of what coded bot will look like
 *
 * @author Jessica
 *
 */
public class SmallRandomBot extends TankRobot
{

    private int movement = 0;
    private int rotation = 0;
    private boolean turning = true;
    private int retaliating = 0;

    public SmallRandomBot()
    {
        super();
    }

    @Override
    public void init()
    {
        setType("smallTank");
    }

    @Override
    public void run()
    {
        float[][] visibles = events.getVisibleBots();
        if (events.hitWall())
        {
            turning = false;
            movement = 50;
            rotation = 23;
            if (retaliating != 0)
            {
                retaliating = -retaliating;
            } else
            {
                retaliating = -1;
            }
        }
        if (visibles.length > 0)
        {
            float bearing = visibles[0][0];
            float elevation = visibles[0][1]-events.barrel;
            float diff = bearing*bearing+elevation*elevation;
            final float give = 0.6f;

            if (Float.compare(diff, give*give) < 0)
            {
                attack();
                return;
            }
            
            if (Float.compare(bearing, 0) < 0)
            {
                aimAntiClockwise();
            }
            if (Float.compare(bearing, 0) > 0)
            {
                aimClockwise();
            }

            if (Float.compare(elevation, 0) < 0)
            {
                aimDown();
            }
            if (Float.compare(elevation, 0) > 0)
            {
                aimUp();
            }
            return;
        }
        if (retaliating != 0)
        {
            retaliate();
        } else if (turning)
        {
            turn();
        }
        else
        {
            move();
        }
    }

    private void turn()
    {
        if (rotation > 0)
        {
            turnAntiClockwise();
            rotation--;
        } else
        {
            rotation = (int) (Math.random() * 180);
            turning = false;
        }
    }

    private void move()
    {
        if (movement > 0)
        {
            moveForward();
            movement--;
        } else
        {
            movement = (int) (Math.random() * 400);
            turning = true;
        }
    }

    private void retaliate()
    {
        if (turning)
        {
            if (rotation > 0)
            {
                if (retaliating == 1)
                {
                    turnClockwise();
                } else
                {
                    turnAntiClockwise();
                }
                rotation--;
            } else
            {
                retaliating = 0;
                turning = !turning;
            }
        } else
        {
            if (movement > 0)
            {
                if (retaliating == 1)
                {
                    moveForward();
                } else
                {
                    moveBackwards();
                }
                movement--;
            } else
            {
                retaliating = 0;
                turning = !turning;
            }
        }
    }
}
